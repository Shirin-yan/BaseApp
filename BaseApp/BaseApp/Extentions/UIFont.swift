import UIKit.UIFont

extension UIFont {
    
    class func regular(size: CGFloat) -> UIFont? {
        return UIFont(name: "name", size: size)
    }
    
    class func medium(size: CGFloat) -> UIFont? {
        return UIFont(name: "name", size: size)
    }

    class func semibold(size: CGFloat) -> UIFont? {
        return UIFont(name: "name", size: size)
    }

    class func bold(size: CGFloat) -> UIFont? {
        return UIFont(name: "name", size: size)
    }
}
